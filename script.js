// DOM элементы
const difficultyButtons = document.querySelectorAll('.difficulty-btn');
const wordDisplay = document.getElementById('wordDisplay');
const wordElement = document.getElementById('word');
const difficultyBadge = document.getElementById('difficultyBadge');
const nextBtn = document.getElementById('nextBtn');
const gameScreen = document.getElementById('gameScreen');

// Текущий уровень сложности
let currentDifficulty = null;

// Обработчики для кнопок сложности
difficultyButtons.forEach(button => {
    button.addEventListener('click', () => {
        currentDifficulty = button.dataset.level;
        showWord(currentDifficulty);
    });
});

// Обработчик для кнопки "Угадано"
nextBtn.addEventListener('click', () => {
    document.querySelector('.difficulty-selector').classList.remove('hidden');
    wordDisplay.classList.add('hidden');
    wordDisplay.style.display = 'none';
});

// Показать случайное слово
function showWord(difficulty) {
    const words = wordsData[difficulty];
    const randomWord = words[Math.floor(Math.random() * words.length)];
    
    wordElement.textContent = randomWord;
    
    // Установка цвета и текста для бейджа сложности
    switch(difficulty) {
        case 'easy':
            difficultyBadge.textContent = 'Легко';
            difficultyBadge.style.backgroundColor = '#4caf50';
            break;
        case 'medium':
            difficultyBadge.textContent = 'Средне';
            difficultyBadge.style.backgroundColor = '#ff9800';
            break;
        case 'hard':
            difficultyBadge.textContent = 'Сложно';
            difficultyBadge.style.backgroundColor = '#f44336';
            break;
    }
    
    // Показываем экран с словом
    document.querySelector('.difficulty-selector').classList.add('hidden');
    wordDisplay.classList.remove('hidden');
    wordDisplay.style.display = 'flex';
}

// Адаптация высоты для мобильных устройств
function adjustHeight() {
    const vh = window.innerHeight * 0.01;
    document.documentElement.style.setProperty('--vh', `${vh}px`);
}

window.addEventListener('resize', adjustHeight);
adjustHeight();